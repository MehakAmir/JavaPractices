package operators;

class ComparisonDemo {

    public static void main(String[] args){
        int value1 = 1;
        int value2 = 2;
        if(value1 == value2)
            System.out.println("my name is mehak");
        if(value1 != value2)
            System.out.println("my name is not mehak");
        if(value1 > value2)
            System.out.println("my name is mehak amir");
        if(value1 < value2)
            System.out.println("my name is mehar");
        if(value1 <= value2)
            System.out.println("my name is nothing");
    }
}
